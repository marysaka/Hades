use hs_gba::BackupStorageType;

#[derive(Copy, Clone, Eq, PartialEq, Ord, PartialOrd, Hash, Debug)]
pub struct GameDatabaseEntry {
    code: &'static str,
    title: &'static str,
    backup_storage_type: Option<BackupStorageType>,
    rtc: bool,
    solar_sensor: bool,
    tilt_sensor: bool,
    gyro_sensor: bool,
    rumble: bool,
    ereader: bool,
}

impl GameDatabaseEntry {
    pub(crate) const fn from(
        code: &'static str,
        backup_storage_type: Option<BackupStorageType>,
        title: &'static str,
    ) -> Self {
        Self {
            code,
            title,
            backup_storage_type,
            rtc: false,
            solar_sensor: false,
            tilt_sensor: false,
            gyro_sensor: false,
            rumble: false,
            ereader: false,
        }
    }

    pub const fn code(&self) -> &'static str {
        &self.code
    }

    pub const fn title(&self) -> &'static str {
        &self.title
    }

    pub const fn backup_storage_type(&self) -> Option<BackupStorageType> {
        self.backup_storage_type
    }

    pub const fn has_rtc(&self) -> bool {
        self.rtc
    }

    pub const fn has_solar_sensor(&self) -> bool {
        self.solar_sensor
    }

    pub const fn has_tilt_sensor(&self) -> bool {
        self.tilt_sensor
    }

    pub const fn has_gyro_sensor(&self) -> bool {
        self.gyro_sensor
    }

    pub const fn has_rumble(&self) -> bool {
        self.rumble
    }

    pub const fn has_ereader(&self) -> bool {
        self.ereader
    }
}

macro_rules! db_entry {
    ($code:expr, $backup:expr, $title:expr) => {
        GameDatabaseEntry::from($code, $backup, $title)
    };
    ($code:expr, $backup:expr, $title:expr, $($device_name:ident = $device_value:expr),* $(,)?) => {{
        GameDatabaseEntry {
            $(
                $device_name: $device_value,
            )*
            ..GameDatabaseEntry::from($code, $backup, $title)
        }
    }};
}

